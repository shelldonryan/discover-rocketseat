let one = 1
let two = 2
// igual a "=="
console.log(one == 2)
console.log(two == '2')
// diferente de "!="
console.log(two != one)
console.log(two != 2)
console.log(two != '2')
// estritamente igual "==="
console.log(one === '1')
console.log(one === 1)
// estritamente diferente "!=="
console.log(two !== '2')
console.log(two !== 2)
// maior que ">" maior igual a ">="
console.log(one > two)
console.log(one >= 1)
console.log(two >= 1)
// menor que "<" menor igual a "<="
console.log(one < two)
console.log(one <= two)
console.log(one <= 1)
